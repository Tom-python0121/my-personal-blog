/**
 * 生成随机日期
 * @param {number} startYear 开始年份
 * @param {number} endYear 结束年份
 * @returns {string} ISO格式的日期字符串
 */
function randomDate(startYear = 2023, endYear = 2024) {
  const start = new Date(startYear, 0, 1);
  const end = new Date(endYear, 11, 31);
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime())).toISOString().split('T')[0];
}

/**
 * 生成随机评分 (1-5)
 * @returns {number} 评分值，精确到小数点后一位
 */
function randomRating() {
  return Math.round((4 + Math.random() * 1) * 10) / 10; // 4.0-5.0之间
}

